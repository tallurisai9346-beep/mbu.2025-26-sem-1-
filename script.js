function randomCGPA() {
  return (Math.random() * (9.1 - 7.5) + 7.5).toFixed(2);
}

function randomServerError() {
  const codes = [500, 502, 503];
  return codes[Math.floor(Math.random() * codes.length)];
}

/* =====================================================
   MAIN RESULT FUNCTION
===================================================== */
function checkResult() {
  const roll = document.getElementById("roll").value.trim().toUpperCase();
  const branch = document.getElementById("branch").value;

  const loader = document.getElementById("loader");
  const errorBox = document.getElementById("errorBox");
  const resultBox = document.getElementById("result");
  const normalTroll = document.getElementById("trollSection");
  const revalTroll = document.getElementById("revalTroll");
  const revalSection = document.getElementById("revalSection");

  errorBox.style.display = "none";
  resultBox.style.display = "none";
  normalTroll.style.display = "none";
  revalTroll.style.display = "none";
  revalTroll.classList.remove("animate");
  revalSection.style.display = "none";

  if (!roll || !branch) {
    errorBox.textContent = "Please enter Roll Number and select Branch";
    errorBox.style.display = "block";
    return;
  }

  let realBranch = null;
  let studentName = null;

  for (const b in students) {
    if (students[b][roll]) {
      realBranch = b;
      studentName = students[b][roll];
      break;
    }
  }

  if (!realBranch) {
    errorBox.textContent = "Results not found";
    errorBox.style.display = "block";
    return;
  }

  if (realBranch !== branch) {
    loader.style.display = "block";
    setTimeout(() => {
      loader.style.display = "none";
      errorBox.textContent = `Server Error (${randomServerError()})`;
      errorBox.style.display = "block";
    }, 1500);
    return;
  }

  loader.style.display = "block";

  setTimeout(() => {
    loader.style.display = "none";
    resultBox.style.display = "block";

    document.getElementById("showRoll").textContent = roll;
    document.getElementById("showName").textContent = studentName;
    document.getElementById("showBranch").textContent = branch;

    const table = document.getElementById("resultTable");
    table.innerHTML = "<tr><th>SUBJECT</th><th>STATUS</th></tr>";

    const subjects = branchSubjects[branch];
    const failOne = Math.random() < 0.4;
    const failedSubject = subjects[Math.floor(Math.random() * subjects.length)];

    let hasFail = false;

    subjects.forEach(sub => {
      let status = "PASS";
      if (failOne && sub === failedSubject) {
        status = "FAIL";
        hasFail = true;
      }
      table.innerHTML += `
        <tr>
          <td>${sub}</td>
          <td class="${status === "PASS" ? "pass" : "fail"}">${status}</td>
        </tr>`;
    });

    const statusText = document.getElementById("statusText");
    const cgpaText = document.getElementById("cgpaText");
    const trollImg = document.getElementById("trollImg");

    if (hasFail) {
      statusText.textContent = "RESULT: FAIL ";
      cgpaText.textContent = "CGPA: NA";
      trollImg.src = TROLL_FAIL;

      revalSection.style.display = "block";
      normalTroll.style.display = "none";
    } else {
      statusText.textContent = "RESULT: PASS ";
      cgpaText.textContent = "CGPA: " + randomCGPA();
      trollImg.src = TROLL_PASS;

      normalTroll.style.display = "block";
    }
  }, 2000);
}

